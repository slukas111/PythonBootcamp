# Integer & Float variables (Numbers)

#print of a variable without words in a print
numeric_variable = 10
print(numeric_variable)

# Define a simple variable
simple_number_a = 10
simple_number_b = 5
print("Simple variable : "+str(simple_number_a))


# Perform a summery of two numbers
summery_result = simple_number_a + simple_number_b
print("Summery result : "+str(summery_result))
# print("summery result " + str(summery_result))

# # Multiply two numbers
multiply_result = simple_number_a * simple_number_b
print("Multiply result : " +str(multiply_result))

# # Divide two numbers
deviation_result = simple_number_a / simple_number_b
print("Deviation result : " +str(deviation_result))

# # power action on two numbers
power_result = simple_number_b ** 2
print("Power result : " +str(power_result))

# # Modulo action on two numbers, gives the remainder after division
modulo_result = simple_number_b % 2
print("Modulo result : " +str(modulo_result))

# # Decimal number variable
decimal_variable_a = 2.5
decimal_variable_b = 0.25

summery_of_two_float = decimal_variable_a + decimal_variable_b
print("Print of decimal variables : " +str(summery_of_two_float))

# # Apply mathematical actions upon a variable itself
multiply_on_a_variable = 5 * 4
